public class Twelve {
  public static void main(String[] args) {
    int a = 10;
    System.out.println(a);
    a += 19; // a = a + 19
    System.out.println(a);

    int b = 0;
    b += 19; // b = b + 19
    System.out.println(b);

    a -= 10; // a = a - 10
    System.out.println(a);

    a *= 2; // a = a * 2
    System.out.println(a);

    int c = 2;
    c /= 2; // c = c / 2
    System.out.println(c);

    int d = 3;
    d %= 2; // d = d % 2 ( % --> Modulo)
    System.out.println(d);
  }
}
